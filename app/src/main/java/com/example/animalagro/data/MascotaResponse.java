package com.example.animalagro.data;

public class MascotaResponse {
        private int id;
        private String tipo;
        private String raza;
        private String nombre;
        private String foto;

        public int getId() {
            return id;
        }

        public String getTipo() {
            return tipo;
        }

        public String getRaza() {
            return raza;
        }

        public String getNombre() {
            return nombre;
        }

    public String getFoto() {
        return foto;
    }
}
